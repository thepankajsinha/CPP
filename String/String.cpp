#include <iostream>
using namespace std;

int main(){
    //Concatenation
    //The + operator can be used to add two string together to make a new string.
    string firstName = "Pankaj ";
    string lasttName = "Sinha ";
    string fullname = firstName + lasttName;
    cout<<fullname;   //Pankaj Sinha

    cout<<endl;

    //string length
    cout<<firstName.length();
    //7

    cout<<endl;

    //Access Strings
    cout<<firstName[0];
    //P

}

